const step4 = async () => {
  const currentMajorVersion = parseInt(packageJson.version.match(/^\d+/)[0])
  const versionNumber = currentMajorVersion + 1

  const scopes = await query(`
    SELECT s1.*, s2.name AS cluster_scope_name
    FROM scopes s1
      INNER JOIN scopes s2 ON s1.cluster_scope_id = s2.id
  `)

  const clusteredScopeToScopes = {}

  scopes.forEach(scope => {
    if (!clusteredScopeToScopes[scope.cluster_scope_name]) {
      clusteredScopeToScopes[scope.cluster_scope_name] = []
    }

    if (scope.cluster_scope_name === scope.name) return

    clusteredScopeToScopes[scope.cluster_scope_name].push(scope.name)
  })

  // requiredScopeNamesFromManualTesting.forEach(scopeName => {
  //   clusteredScopeToScopes[scopeName] = []
  // })

  // const fakeScopeMatchingTheme = {
  //   displayName: "Fake Theme For Scope Matching",
  //   name: "fake-scope-matching-theme",
  //   tokenColors: [],
  // }

  // const convertNumberToCssHex = number => "#" + (number + 1).toString().padStart(6, "0")

  const database = {}

  const semanticTokenMappings = {}

  Object.entries(clusteredScopeToScopes).forEach(
    ([primaryScopeName, secondaryScopeNames], index) => {
      const color = `color${index + 1}`
      const colorVersion = `color${index + 1}.version${versionNumber}`

      semanticTokenMappings[colorVersion] = [primaryScopeName]

      database[primaryScopeName] = color
      secondaryScopeNames.forEach(secondaryScopeName => {
        database[secondaryScopeName] = color
      })

      // const fakeCssHex = convertNumberToCssHex(index)

      // fakeScopeMatchingTheme.tokenColors.push({
      //   scope: [primaryScopeName, ...secondaryScopeNames],
      //   settings: { foreground: fakeCssHex },
      // })
    },
  )

  const contributes = {
    configurationDefaults: { "editor.semanticHighlighting.enabled": true },
    semanticTokenScopes: [{ scopes: semanticTokenMappings }],
  }

  const colors = []
  for (let i = 1; i <= Object.keys(clusteredScopeToScopes).length; i += 1) {
    colors.push(`color${i}`)
  }

  const capabilities = {
    textDocumentSync: 1,
    semanticTokensProvider: {
      full: true,
      legend: { tokenModifiers: [`version${versionNumber}`], tokenTypes: colors },
    },
  }

  const contributesMd = `<!-- AUTOGENERATED FILE! EDIT IN build-database/step-4-finalize.js -->
# Contributes

Please add the following to the "contributes" section in your extension's package.json:

\`\`\`json
${JSON.stringify(contributes, null, 2)}
\`\`\`
`

  const capabilitiesMd = `<!-- AUTOGENERATED FILE! EDIT IN build-database/step-4-finalize.js -->
# Capabilities

Please add the following to your language server's initialization response. 

If you want, you can 
import the list of colors using:
\`\`\`js
const colors = require('lsp-syntax-highlighter/colors.json')\`
\`\`\`

\`\`\`json
${JSON.stringify(capabilities, null, 2)}
\`\`\`
`

  await fs.writeFile(
    path.resolve(__dirname, "../database.json"),
    JSON.stringify(database, null, 2),
    { encoding: "utf8" },
  )

  // await fs.writeFile(
  //   path.resolve(__dirname, "../database.json"),
  //   JSON.stringify(fakeScopeMatchingTheme, null, 2),
  //   { encoding: "utf8" },
  // )

  await fs.writeFile(path.resolve(__dirname, "../contributes.md"), contributesMd, {
    encoding: "utf8",
  })

  await fs.writeFile(path.resolve(__dirname, "../capabilities.md"), capabilitiesMd, {
    encoding: "utf8",
  })

  await fs.writeFile(path.resolve(__dirname, "../colors.json"), JSON.stringify(colors, null, 2), {
    encoding: "utf8",
  })
}

const db = new sqlite3.Database("./data.db", err => {
  if (err) throw err
})

const query = async (sql, parameters = []) => {
  return new Promise(resolve => {
    db.all(sql, parameters, (err, rows) => {
      if (err) throw err
      resolve(rows)
    })
  })
}

step4()
